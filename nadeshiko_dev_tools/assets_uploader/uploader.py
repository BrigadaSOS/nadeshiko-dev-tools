class UploadHistory:
    """Tracks which media/episodes have been successfully uploaded per environment.

    History is stored as a JSON file at {root_dir}/_upload_history_{env}.json.
    Structure: { "<media_id>": { "episodes": [1, 2, 3], "completed": false } }
    A media is "completed" when all its episodes have been uploaded successfully.
    """

    def __init__(self, root_dir: Path, env: str):
        self.path = root_dir / f"_upload_history_{env}.json"
        self._data: dict[str, dict] = {}
        self._load()

    def _load(self) -> None:
        if self.path.exists():
            with open(self.path) as f:
                self._data = json.load(f)

    def _save(self) -> None:
        with open(self.path, "w") as f:
            json.dump(self._data, f, indent=2, sort_keys=True)

    def is_media_complete(self, media_id: str) -> bool:
        return self._data.get(media_id, {}).get("completed", False)

    def is_episode_done(self, media_id: str, episode: int) -> bool:
        return episode in self._data.get(media_id, {}).get("episodes", [])

    def mark_episode_done(self, media_id: str, episode: int) -> None:
        entry = self._data.setdefault(media_id, {"episodes": [], "completed": False})
        if episode not in entry["episodes"]:
            entry["episodes"].append(episode)
            entry["episodes"].sort()
        self._save()

    def mark_media_complete(self, media_id: str) -> None:
        entry = self._data.setdefault(media_id, {"episodes": [], "completed": False})
        entry["completed"] = True
        self._save()


class NadeshikoUploader:
    """Handles uploading to Nadeshiko API and R2."""

    # Maximum content length for segments (characters)
    MAX_CONTENT_LENGTH = 500

    def __init__(
        self,
        config: Config,
        dry_run: bool = False,
        storage_target: str = "local",
        upload_r2: bool = False,
        update_info_only: bool = False,
        history: UploadHistory | None = None,
    ):
        _validate_sdk_contract()
        self.config = config
        self.dry_run = dry_run
        self.storage_target = storage_target
        self.upload_r2 = upload_r2
        self.update_info_only = update_info_only
        self.history = history
        # Only initialize R2Uploader when we'll actually use it
        self.r2 = R2Uploader(config) if upload_r2 else None

        self.api_client = Nadeshiko(
            token=config.nadeshiko_api_key,
            base_url=config.nadeshiko_base_url,
        )
        self.ErrorTypes = API_ERROR_TYPES
        self._console_lock = threading.Lock()
        self.stats = {
            "total": 0,
            "uploaded": 0,
            "duplicated": 0,
            "skipped_no_hash": 0,
            "skipped_no_translations": 0,
            "skipped_too_long": 0,
            "skipped_constraint": 0,
            "failed": 0,
            "errors_by_status": {},  # Track errors by HTTP status code
        }

        if dry_run:
            console.print(
                "[cyan]Running in DRY-RUN mode. Use --apply to perform actual uploads.[/cyan]"
            )

    def _ms_to_hmsff(self, milliseconds: int) -> str:
        """Convert milliseconds to H:MM:SS.ffffff format."""
        td = timedelta(milliseconds=milliseconds)
        total_seconds = td.total_seconds()
        hours = int(total_seconds // 3600)
        minutes = int((total_seconds % 3600) // 60)
        seconds = total_seconds % 60
        return f"{hours}:{minutes:02d}:{seconds:06f}"

    def _get_error_attr(self, error: object, attr_name: str):
        """Safely read SDK error attributes that may raise KeyError."""
        try:
            val = getattr(error, attr_name)
            # Typed status enums (e.g. Error400Status) → unwrap to int
            if attr_name == "status" and hasattr(val, "value"):
                return val.value
            return val
        except (KeyError, AttributeError):
            return None

    def _is_api_error(self, value: object) -> bool:
        """Check whether a response is an SDK error (public or internal)."""
        return isinstance(value, self.ErrorTypes)

    def _is_auth_error(self, error: object) -> bool:
        """Check for authentication errors returned by the API."""
        return isinstance(error, Error401)

    def _print_api_error(self, error: object) -> None:
        """Print standard API error details."""
        for attr_name in ["detail", "status", "code", "title", "errors"]:
            value = self._get_error_attr(error, attr_name)
            if value:
                self._print(f"  [red]{attr_name.capitalize()}:[/red] {value}")

    def _is_rate_limit_error(self, error: object) -> bool:
        """Check if an error is a 429 rate limit error."""
        return isinstance(error, Error429)

    def _is_constraint_error(self, error: object) -> bool:
        """Check if an error is a payload validation error for a specific segment.

        Only HTTP 400 is considered skippable. Other 4xx values (forbidden/not found)
        usually indicate auth or upstream consistency issues that should fail loudly.
        """
        return isinstance(error, Error400)

    def _get_retry_after_seconds(self, error: object) -> int:
        """Get the retry-after seconds from a rate limit error.

        We use a fixed 60-second wait for all rate limit errors to keep
        things simple and reliable.
        """
        return _RATE_LIMIT_WAIT_SECONDS

    def _should_skip_segment(self, segment: SegmentData) -> tuple[bool, str]:
        """Check if a segment should be skipped and return (skip, reason)."""
        # Check if segment has a valid hash
        if not segment.segment_hash:
            return True, "no segment hash"

        # Check if segment has all three translations
        if not segment.content_es:
            return True, "no Spanish translation"
        if not segment.content_en:
            return True, "no English translation"

        # Check if any content field is too long
        for field, value in [
            ("ja", segment.content_ja),
            ("en", segment.content_en),
            ("es", segment.content_es),
        ]:
            if value and len(value) > self.MAX_CONTENT_LENGTH:
                return True, f"content too long ({field}: {len(value)} > {self.MAX_CONTENT_LENGTH})"

        return False, ""

    def _segment_content_rating(self, segment: SegmentData):
        """Convert text content rating to SDK enum, or UNSET when unknown."""
        rating = (segment.content_rating or "").strip().upper()
        if not rating:
            return UNSET
        try:
            return ContentRating(rating)
        except ValueError:
            return UNSET

    def _coerce_int(self, value: object) -> int | None:
        """Coerce values from _info.json into an integer ID."""
        if isinstance(value, bool):
            return None
        if isinstance(value, int):
            return value
        if isinstance(value, str):
            stripped = value.strip()
            if stripped and stripped.isdigit():
                return int(stripped)
        return None

    def _parse_character_role(self, value: object) -> CharacterInputRole:
        """Parse AniList role values from _info.json into SDK enum values."""
        if isinstance(value, CharacterInputRole):
            return value
        role_text = str(value or "").upper()
        for role in CharacterInputRole:
            if role.value in role_text:
                return role
        return CharacterInputRole.SUPPORTING

    def _build_character_inputs(self, media_info: MediaInfo) -> list[CharacterInput] | None:
        """Build SDK CharacterInput objects from _info.json character records.

        Returns None when characters were absent in _info.json so we don't
        accidentally overwrite server-side character data.

        Currently disabled - character upload is temporarily disabled.
        """
        return None

    def _print(self, *args, **kwargs) -> None:
        """Thread-safe console print."""
        with self._console_lock:
            console.print(*args, **kwargs)

    def _upload_media_images(self, media_folder: Path, storage_base_path: str) -> None:
        """Upload cover and banner images to R2 if they exist locally."""
        if not self.upload_r2 or not self.r2:
            return

        for prefix in ("cover", "banner"):
            image_path = _find_image_file(media_folder, prefix)
            if not image_path:
                continue
            if not self.r2.media_file_exists(storage_base_path, image_path.name):
                self.r2.upload_media_file(image_path, storage_base_path)
                console.print(f"[green]Uploaded {prefix}: {image_path.name}[/green]")
            else:
                console.print(
                    f"[dim]{prefix.capitalize()} already uploaded: {image_path.name}[/dim]"
                )

    def _update_media_info(
        self, existing_media, media_info: MediaInfo,
        media_folder: Path, storage_base_path: str = "",
    ) -> int | None:
        """Update existing media with storage backend and other info."""
        if self.dry_run:
            console.print(f"[cyan][DRY RUN] Would update media: {existing_media.id}[/cyan]")
            return existing_media.id

        console.print(f"[cyan]Updating media info: {existing_media.id}[/cyan]")

        storage = (
            MediaUpdateRequestStorage.R2
            if self.storage_target == "r2"
            else MediaUpdateRequestStorage.LOCAL
        )

        self._upload_media_images(media_folder, storage_base_path)

        start_date = _parse_iso_date(media_info.start_date)
        end_date = _parse_iso_date(media_info.end_date)

        # Build update request with only fields that are set
        request = MediaUpdateRequest()
        request.storage = storage
        request.external_ids = ExternalId(anilist=str(media_info.anilist_id))
        if start_date is not UNSET:
            request.start_date = start_date
        if end_date is not UNSET:
            request.end_date = end_date
        if media_info.studio:
            request.studio = media_info.studio
        if media_info.season_name:
            request.season_name = media_info.season_name
        if media_info.season_year is not None:
            request.season_year = media_info.season_year
        if media_info.hash_salt:
            request.hash_salt = media_info.hash_salt
        if media_info.version:
            request.version = media_info.version
        character_inputs = self._build_character_inputs(media_info)
        if character_inputs is not None:
            request.characters = character_inputs
        if storage_base_path:
            request.storage_base_path = storage_base_path

        update_result = update_media.sync(
            client=self.api_client, id=existing_media.id, body=request
        )

        if update_result and not self._is_api_error(update_result):
            console.print(f"[green]Updated media: {existing_media.id}[/green]")
            return existing_media.id

        if self._is_api_error(update_result):
            console.print("\n[red]Failed to update media:[/red]")
            self._print_api_error(update_result)
        else:
            console.print("[red]Failed to update media: Unknown error[/red]")
        return existing_media.id  # Return ID anyway if update failed

