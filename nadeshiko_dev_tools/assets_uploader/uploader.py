def normalize_r2_url(url: str, r2_public_url: str) -> str:
    """Convert raw R2 URL to CDN URL if needed.

    Args:
        url: The URL to normalize (may be raw R2 or already CDN)
        r2_public_url: The CDN base URL (e.g., https://cdn.nadeshiko.co)

    Returns:
        The normalized URL using the CDN domain
    """
    if not url:
        return url

    # If URL is already using the CDN domain, return as-is
    if url.startswith(r2_public_url):
        return url

    # If URL is a raw R2 URL, extract the path and prepend CDN domain
    if ".r2.cloudflarestorage.com/" in url:
        # Extract path after the bucket/domain
        parts = url.split("/media/", 1)
        if len(parts) == 2:
            return f"{r2_public_url}/media/{parts[1]}"

    return url


IMAGE_EXTENSIONS = (".jpg", ".jpeg", ".png", ".webp")


def _parse_iso_date(date_str: str | None):
    """Parse an ISO date/datetime string to a date, or return UNSET."""
    if not date_str:
        return UNSET
    try:
        normalized = date_str.strip()
        if normalized.endswith("Z"):
            normalized = f"{normalized[:-1]}+00:00"
        parsed = dt.datetime.fromisoformat(normalized)
        return parsed.date()
    except (ValueError, TypeError):
        return UNSET


def _find_image_file(folder: Path, prefix: str) -> Path | None:
    """Find an image file like cover.jpg, cover.png, etc. in a folder."""
    for ext in IMAGE_EXTENSIONS:
        path = folder / f"{prefix}{ext}"
        if path.exists():
            return path
    return None


def _find_japanese_stream(audio_streams: list[dict]) -> dict | None:
    """Find a Japanese audio stream from a list of audio stream dicts."""
    for stream in audio_streams:
        lang = stream.get("tags", {}).get("language", "").lower()
        if lang in ("jpn", "ja", "japanese"):
            return stream
    return None


@dataclass
class Config:
    """Configuration from environment variables."""

    nadeshiko_api_key: str
    nadeshiko_base_url: str
    r2_account_id: str
    r2_access_key_id: str
    r2_secret_access_key: str
    r2_bucket: str
    r2_public_url: str
    target: str = "local"
    is_prod: bool = False

    @classmethod
    def from_env(cls, env: str = "local") -> "Config":
        if env not in ("local", "dev", "prod"):
            raise ValueError(f"Invalid environment: {env}")

        def _first_set(*names: str, default: str = "") -> str:
            for name in names:
                value = os.getenv(name)
                if value:
                    return value
            return default

        # R2 config is shared across all environments
        r2_kwargs = {
            "r2_account_id": os.getenv("R2_ACCOUNT_ID", ""),
            "r2_access_key_id": os.getenv("R2_ACCESS_KEY_ID", ""),
            "r2_secret_access_key": os.getenv("R2_SECRET_ACCESS_KEY", ""),
            "r2_bucket": os.getenv("R2_BUCKET", "nadeshiko-segments"),
            "r2_public_url": os.getenv("R2_PUBLIC_URL", ""),
        }

        env_config = {
            "local": {
                "nadeshiko_api_key": _first_set("NADESHIKO_LOCAL_API_KEY", "NADESHIKO_API_KEY"),
                "nadeshiko_base_url": _first_set(
                    "NADESHIKO_LOCAL_BASE_URL", "NADESHIKO_BASE_URL",
                    default="http://localhost:5000",
                ),
                "is_prod": False,
            },
            "dev": {
                "nadeshiko_api_key": _first_set("NADESHIKO_DEV_API_KEY", "NADESHIKO_API_KEY"),
                "nadeshiko_base_url": _first_set(
                    "NADESHIKO_DEV_BASE_URL", default="https://api-dev.nadeshiko.co",
                ),
                "is_prod": False,
            },
            "prod": {
                "nadeshiko_api_key": os.getenv("NADESHIKO_PROD_API_KEY", ""),
                "nadeshiko_base_url": os.getenv(
                    "NADESHIKO_PROD_BASE_URL", "https://api.nadeshiko.co"
                ),
                "is_prod": True,
            },
        }

        return cls(**env_config[env], **r2_kwargs, target=env)

    def validate(self, storage_target: str = "local", upload_r2: bool = False) -> bool:
        """Check that required config is present.

        Args:
            storage_target: Where media files should be served from ("local" or "r2").
            upload_r2: Whether files will be uploaded to R2. If False, R2 credentials are optional.
        """
        missing = []
        if not self.nadeshiko_api_key:
            if self.target == "local":
                missing.append("NADESHIKO_LOCAL_API_KEY (or NADESHIKO_API_KEY)")
            elif self.target == "dev":
                missing.append("NADESHIKO_DEV_API_KEY (or NADESHIKO_API_KEY)")
            else:
                missing.append("NADESHIKO_PROD_API_KEY")

        # R2 credentials are required only when files are uploaded to R2.
        needs_r2 = upload_r2

        if needs_r2:
            if not self.r2_public_url:
                missing.append("R2_PUBLIC_URL")
            if not self.r2_account_id:
                missing.append("R2_ACCOUNT_ID")
            if not self.r2_access_key_id:
                missing.append("R2_ACCESS_KEY_ID")
            if not self.r2_secret_access_key:
                missing.append("R2_SECRET_ACCESS_KEY")
            if not self.r2_bucket:
                missing.append("R2_BUCKET")

        if missing:
            console.print(f"[red]Missing required env vars: {', '.join(missing)}[/red]")
            return False
        return True

    def display_target(self) -> None:
        """Display the upload target with appropriate styling."""
        if self.is_prod:
            console.print(
                "\n[bold red on white] TARGET: PRODUCTION [/bold red on white]\n"
                "[bold red] This will upload to PRODUCTION! [/bold red]"
            )
        elif self.target == "dev":
            console.print(
                "\n[bold cyan] TARGET: DEV [/bold cyan]\n"
                f"[dim] API: {self.nadeshiko_base_url}[/dim]"
            )
        else:
            console.print(
                "\n[bold green] TARGET: LOCAL [/bold green]\n"
                f"[dim] API: {self.nadeshiko_base_url}[/dim]"
            )


